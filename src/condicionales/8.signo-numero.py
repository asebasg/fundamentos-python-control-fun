numero = 0 # Se declara la variable numero con valor 0

if numero > 0: # Se verifica si el numero es mayor que 0
    print("El número es positivo.") # Si es positivo, imprime este mensaje
elif numero < 0: # Si no es positivo, verifica si es menor que 0
    print("El número es negativo.") # Si es negativo, imprime este mensaje
else: # Si no es positivo ni negativo
    print("El número es cero.") # Imprime que es cero
