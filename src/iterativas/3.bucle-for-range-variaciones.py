# Números del 3 al 7
for i in range(3, 8):  # Se itera desde 3 hasta 7 usando range(3, 8)
    print(i, end=" ")  # Se imprime cada número seguido de un espacio

print()  # Salto de línea

# Números pares del 2 al 10
for i in range(2, 11, 2):  # Se itera desde 2 hasta 10 con paso 2 (pares)
    print(i, end=" ")  # Se imprime cada número par seguido de un espacio

print()  # Salto de línea

# Cuenta regresiva
for i in range(10, 0, -1):  # Se itera desde 10 hasta 1 con paso -1 (regresiva)
    print(i, end=" ")  # Se imprime cada número seguido de un espacio
