usuario = {"nombre": "Laura", "edad": 28, "ciudad": "Madrid"}  # Se crea un diccionario con datos de usuario

# Iterando sobre claves
for clave in usuario:  # Se itera sobre las claves del diccionario
    print(f"Clave: {clave}, Valor: {usuario[clave]}")  # Se imprime la clave y su valor correspondiente
