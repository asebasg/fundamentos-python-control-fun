for numero in range(1, 11):  # Bucle for desde 1 hasta 10
    if numero == 5:  # Si el número es 5
        print("¡Encontrado el 5! Saliendo del bucle...")  # Imprime mensaje
        break  # Rompe el bucle
    print(f"Número actual: {numero}")  # Imprime el número actual

print("Bucle terminado")  # Imprime al finalizar
